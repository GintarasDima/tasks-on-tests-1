def is_leap(year): # определяем условия через функцию
    if year % 4 == 0 and year % 100 != 0 or year % 400 == 0:
        return "Высокосный"
    else:
        return "Обычный"
year = int(input("Введите год с 1900 по 3000: ")) # просим ввести год и сразу преобразуем в нужный формат
if 1900 <= year <= 3000: # проверяем правильность ввода
    print(is_leap(year)) # выводим на печать путем вызова функции
else: # в случае не правильного ввода
    print("Ввели не правильный год, повторите еще")
